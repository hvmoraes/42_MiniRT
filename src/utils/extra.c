#include "../inc/minirt.h"

int	is_number(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if ((str[i] < '0' || str[i] > '9') && str[i] != ',' && str[i] != '.')
			return (0);
		i++;
	}
	return (1);
}
